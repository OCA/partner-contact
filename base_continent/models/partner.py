from openerp.osv.orm import Model
from openerp.osv import fields


class Partner(Model):
    _inherit = 'res.partner'
    _columns = {
        'continent_id': fields.related('country_id', 'continent_id',
                                       type='many2one',
                                       relation='res.continent',
                                       string='Continent',
                                       readonly=True, store=True),
    }
